#include "mesh_header.h"

//============================================================================

Point::Point(const int *ind, int attr) : Element()
{
  attribute = attr;
  indices[0] = ind[0];
}

//============================================================================

void Point::SetVertices(const int *ind)
{
  indices[0] = ind[0];
}

//============================================================================

void Point::GetVertices(Array<int> &v) const
{
  v.SetSize(1);
  v[0] = indices[0];
}

//============================================================================

Point::~Point() {;}

